def list_notebook_data_assets() -> list[NotebookDataAsset]:
    """Return known notebook data assets."""

    return list(NOTEBOOK_DATA_ASSETS)


def _csv_row_count(path: Path) -> int | None:
    """Return CSV row count when possible."""

    if path.suffix.lower() != ".csv" or not path.exists():
        return None

    with path.open("r", encoding="utf-8", newline="") as file:
        reader = csv.DictReader(file)
        return sum(1 for _ in reader)


def _asset_explorable(project_root: Path, asset: NotebookDataAsset) -> bool:
    """Return True if an asset can be used safely for notebook exploration."""

    path = project_root / asset.relative_path

    if not path.exists():
        return False

    if asset.asset_id == "model_training_table":
        return validate_training_table_schema(path).is_training_ready

    return True


def build_notebook_data_catalog(project_root: Path) -> NotebookDataCatalogSummary:
    """Build notebook data catalog summary."""

    entries = []

    for asset in NOTEBOOK_DATA_ASSETS:
        path = project_root / asset.relative_path
        exists = path.exists()

        entries.append(
            NotebookDataCatalogEntry(
                asset=asset,
                exists=exists,
                file_size_bytes=path.stat().st_size if exists else 0,
                row_count=_csv_row_count(path),
                explorable=_asset_explorable(project_root, asset),
            )
        )

    return NotebookDataCatalogSummary(entries=entries)


def render_notebook_data_catalog_report(
    summary: NotebookDataCatalogSummary,
) -> str:
    """Render notebook data catalog as Markdown."""

    lines = [
        "# Notebook Data Catalog Report",
        "",
        "## Summary",
        "",
        f"- Assets cataloged: {summary.asset_count}",
        f"- Available assets: {summary.available_count}",
        f"- Explorable assets: {summary.explorable_count}",
        f"- Missing assets: {summary.missing_count}",
        f"- Blocking EDA assets: {summary.blocking_eda_count}",
        f"- Ready for initial EDA: {summary.ready_for_eda}",
        "",
        "## Assets",
        "",
        "| Asset | Category | Path | Required for EDA | Status | Rows | Size Bytes |",
        "|---|---|---|---:|---|---:|---:|",
    ]

    for entry in summary.entries:
        lines.append(
            "| "
            f"{entry.asset.label} | "
            f"{entry.asset.category} | "
            f"{entry.asset.relative_path} | "
            f"{entry.asset.required_for_eda} | "
            f"{entry.status} | "
            f"{entry.row_count if entry.row_count is not None else '-'} | "
            f"{entry.file_size_bytes} |"
        )

    lines.extend(
        [
            "",
            "## Interpretation",
            "",
        ]
    )

    if summary.ready_for_eda:
        lines.append("The available assets are enough to start initial notebook-based EDA.")
    else:
        lines.append("Initial EDA should wait until all required EDA assets are available.")

    lines.extend(
        [
            "",
            "## Training Note",
            "",
            (
                "A model-ready training table is cataloged but not required for initial "
                "EDA. Real ML training should still wait until the training table is "
                "schema-valid, non-empty, and documented."
            ),
        ]
    )

    return "\n".join(lines).rstrip() + "\n"


def write_notebook_data_catalog_report(
    summary: NotebookDataCatalogSummary,
    output_path: Path,
) -> Path:
    """Write notebook data catalog report."""

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        render_notebook_data_catalog_report(summary),
        encoding="utf-8",
    )
    return output_path
