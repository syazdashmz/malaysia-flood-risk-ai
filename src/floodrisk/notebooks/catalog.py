@dataclass(frozen=True)
class NotebookDataAsset:
    """Expected data/report asset for notebook exploration."""

    asset_id: str
    label: str
    relative_path: str
    category: str
    required_for_eda: bool
    description: str

    def as_dict(self) -> dict[str, str | bool]:
        """Return asset metadata as a dictionary."""

        return asdict(self)


@dataclass(frozen=True)
class NotebookDataCatalogEntry:
    """Catalog entry for one data/report asset."""

    asset: NotebookDataAsset
    exists: bool
    file_size_bytes: int
    row_count: int | None
    explorable: bool

    @property
    def status(self) -> str:
        """Return asset status."""

        if self.explorable:
            return "explorable"
        if self.exists:
            return "available_not_explorable"
        return "missing"

    @property
    def blocks_eda(self) -> bool:
        """Return True if missing asset blocks the initial EDA milestone."""

        return self.asset.required_for_eda and not self.explorable

    def as_dict(self) -> dict[str, str | int | bool | dict[str, str | bool] | None]:
        """Return catalog entry as a dictionary."""

        return {
            "asset": self.asset.as_dict(),
            "exists": self.exists,
            "file_size_bytes": self.file_size_bytes,
            "row_count": self.row_count,
            "explorable": self.explorable,
            "status": self.status,
            "blocks_eda": self.blocks_eda,
        }


@dataclass(frozen=True)
class NotebookDataCatalogSummary:
    """Notebook data catalog summary."""

    entries: list[NotebookDataCatalogEntry]

    @property
    def asset_count(self) -> int:
        """Return number of cataloged assets."""

        return len(self.entries)

    @property
    def available_count(self) -> int:
        """Return number of existing assets."""

        return sum(1 for entry in self.entries if entry.exists)

    @property
    def explorable_count(self) -> int:
        """Return number of explorable assets."""

        return sum(1 for entry in self.entries if entry.explorable)

    @property
    def missing_count(self) -> int:
        """Return number of missing assets."""

        return sum(1 for entry in self.entries if not entry.exists)

    @property
    def blocking_eda_count(self) -> int:
        """Return number of missing assets that block EDA."""

        return sum(1 for entry in self.entries if entry.blocks_eda)

    @property
    def ready_for_eda(self) -> bool:
        """Return True if required EDA assets are available."""

        return self.blocking_eda_count == 0

    def as_dict(self) -> dict[str, int | bool | list[dict]]:
        """Return catalog summary as a dictionary."""

        return {
            "asset_count": self.asset_count,
            "available_count": self.available_count,
            "explorable_count": self.explorable_count,
            "missing_count": self.missing_count,
            "blocking_eda_count": self.blocking_eda_count,
            "ready_for_eda": self.ready_for_eda,
            "entries": [entry.as_dict() for entry in self.entries],
        }


